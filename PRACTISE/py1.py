name = "Jay Siya Ram"
# upper case functions 
print(name.upper)
result = name.upper()
print(result)

# lowercase function 
result = name.lower()
print(result)

# Title functions
result = name.title()
print(result)

# capitalize function 
result =  name.capitalize()
print(result)

# Swapcase function : it check whether the string is number or not ..
result = name.isalpha()
print(result)

# isDigit function 
result = name.isdigit()
print(result)

# alnum function : it check whether the string has all the digits and alphabets ....
result = name.isalnum()
print(result)


# isSpace functions 
result = name.isspace()
print(result)

# find function     
result = name.find("Siya")
print(result)

# rindex functions 
result = name.rindex("S")
print(result)

# startsWith function
result = name.startswith("S")
print(result)

# endsWith function 
result = name.endswith("m")
print(result)





